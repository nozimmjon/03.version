def highlight_run(run):
    """Apply yellow highlight to a run using OoXML"""
    from docx.oxml.ns import qn as _qn
    rPr = run._r.get_or_add_rPr()
    highlight_elem = rPr.makeelement(_qn('w:highlight'), {_qn('w:val'): 'yellow'})
    rPr.append(highlight_elem)

def highlight_paragraph(p):
    """Apply yellow highlight to all runs in a paragraph"""
    for run in p.runs:
        highlight_run(run)

def highlight_cell(cell):
    """Apply yellow background shading to a table cell"""
    from docx.oxml.ns import qn as _qn
    tc = cell._tc
    tcPr = tc.get_or_add_tcPr()
    shading = tcPr.makeelement(_qn('w:shd'), {
        _qn('w:fill'): 'FFFF00',
        _qn('w:val'): 'clear',
    })
    tcPr.append(shading)

def add_key_finding(doc, text, yellow=False):
    """Add a highlighted key finding box"""
    p = doc.add_paragraph()
    p.paragraph_format.left_indent = Cm(1)
    p.paragraph_format.right_indent = Cm(1)
    run = p.add_run('КЛЮЧЕВОЙ ВЫВОД: ')
    run.bold = True
    run.font.color.rgb = RGBColor(0x1B, 0x3A, 0x5C)
    run.font.size = Pt(10)
    run2 = p.add_run(text)
    run2.font.size = Pt(10)
    run2.italic = True
    if yellow:
        highlight_run(run)
        highlight_run(run2)

def add_table(doc, headers, rows, title=None, yellow=False):
    """Add a formatted table"""
    if title:
        p = doc.add_paragraph()
        run = p.add_run(title)
        run.bold = True
        run.font.size = Pt(10)
        if yellow:
            highlight_run(run)
    table = doc.add_table(rows=1+len(rows), cols=len(headers))
    table.style = 'Light Shading Accent 1'
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    # Headers
    for i, h in enumerate(headers):
        cell = table.rows[0].cells[i]
        cell.text = h
        for p in cell.paragraphs:
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            for run in p.runs:
                run.bold = True
                run.font.size = Pt(9)
        if yellow:
            highlight_cell(cell)
    # Data
    for r_idx, row in enumerate(rows):
        for c_idx, val in enumerate(row):
            cell = table.rows[r_idx+1].cells[c_idx]
            cell.text = str(val)
            for p in cell.paragraphs:
                p.alignment = WD_ALIGN_PARAGRAPH.CENTER
                for run in p.runs:
                    run.font.size = Pt(9)
            if yellow:
                highlight_cell(cell)
    doc.add_paragraph()

